SET SQL_MODE = "NO_AUTO_VALUE_ON_ZERO";
SET time_zone = "+00:00";

CREATE TABLE IF NOT EXISTS agence(
  ID BIGINT NOT NULL,
  NOM VARCHAR(50) UNIQUE NOT NULL,
  URL VARCHAR(200) NOT NULL,
  FUSEAU_HORAIRE VARCHAR(25) NOT NULL,
  TELEPHONE VARCHAR(50) NOT NULL,
  SIEGE VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS itineraire(
  ID BIGINT NOT NULL,
  AGENCE_ID BIGINT NOT NULL,
  TYPE VARCHAR(50) NOT NULL,
  NOM VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID),
  UNIQUE (NOM, TYPE),
  FOREIGN KEY (AGENCE_ID) REFERENCES agence(ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS service(
  ID BIGINT AUTO_INCREMENT NOT NULL,
  NOM VARCHAR(50) NOT NULL,
  LUNDI TINYINT(1) NOT NULL DEFAULT 0,
  MARDI TINYINT(1) NOT NULL DEFAULT 0,
  MERCREDI TINYINT(1) NOT NULL DEFAULT 0,
  JEUDI TINYINT(1) NOT NULL DEFAULT 0,
  VENDREDI TINYINT(1) NOT NULL DEFAULT 0,
  SAMEDI TINYINT(1) NOT NULL DEFAULT 0,
  DIMANCHE TINYINT(1) NOT NULL DEFAULT 0,
  DATE_DEBUT DATE NOT NULL,
  DATE_FIN DATE NOT NULL,
  CHECK (DATE_DEBUT <= DATE_FIN),
  PRIMARY KEY (ID)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS langueprincipale(
  AGENCE_ID BIGINT NOT NULL,
  LANGUE CHAR(2) NOT NULL,
  PRIMARY KEY (AGENCE_ID, LANGUE),
  FOREIGN KEY (AGENCE_ID) REFERENCES agence(ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS trajet(
  TRAJET_ID VARCHAR(50) NOT NULL,
  SERVICE_ID BIGINT NOT NULL,
  ITINERAIRE_ID BIGINT NOT NULL,
  DIRECTION TINYINT(1) NOT NULL,
  PRIMARY KEY (TRAJET_ID, SERVICE_ID, ITINERAIRE_ID),
  UNIQUE(TRAJET_ID),
  FOREIGN KEY (SERVICE_ID) REFERENCES service(ID) ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (ITINERAIRE_ID) REFERENCES itineraire(ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS arret(
  ID BIGINT NOT NULL,
  NOM VARCHAR(50) NOT NULL,
  LATITUDE FLOAT NOT NULL,
  LONGITUDE FLOAT NOT NULL,
  CHECK (LATITUDE BETWEEN -90.0 AND 90.0),
  CHECK (LONGITUDE BETWEEN -180.0 AND 180.0),
  PRIMARY KEY (ID)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS arret_desservi(
  ITINERAIRE_ID BIGINT NOT NULL,
  ARRET_ID BIGINT NOT NULL,
  `SEQUENCE` INT NOT NULL,
  PRIMARY KEY (ITINERAIRE_ID, ARRET_ID),
  FOREIGN KEY (ITINERAIRE_ID) REFERENCES itineraire(ID) ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (ARRET_ID) REFERENCES arret(ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS exception(
  SERVICE_ID BIGINT NOT NULL,
  `DATE` DATE NOT NULL,
  CODE TINYINT(1) NOT NULL,
  PRIMARY KEY (SERVICE_ID, `DATE`),
  FOREIGN KEY (SERVICE_ID) REFERENCES service(ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

CREATE TABLE IF NOT EXISTS horaire(
  TRAJET_ID VARCHAR(50) NOT NULL,
  ITINERAIRE_ID BIGINT NOT NULL,
  ARRET_ID BIGINT NOT NULL,
  HEURE_ARRIVEE TIME DEFAULT NULL,
  HEURE_DEPART TIME DEFAULT NULL,
  PRIMARY KEY (TRAJET_ID, ITINERAIRE_ID, ARRET_ID),
  FOREIGN KEY (TRAJET_ID) REFERENCES trajet(TRAJET_ID) ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (ITINERAIRE_ID, ARRET_ID) REFERENCES arret_desservi(ITINERAIRE_ID, ARRET_ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;

